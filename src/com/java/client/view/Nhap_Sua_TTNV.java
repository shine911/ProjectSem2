    public Nhap_Sua_TTNV() {
        initComponents();
        nv = new Nhanvien();
        Set<String> danhSachMa = QuanLiNhanSu.danhSachNhanVien.keySet();
        int max = 0;
        for (String maNV : danhSachMa) {
            if (maNV.contains("NV")) {
                int ma = Integer.parseInt(maNV.substring(2));
                if (max < ma) {
                    max = ma;
                }
            }
        }
        nv.setMaNv("NV" + String.format("%03d", max + 1));
    }

